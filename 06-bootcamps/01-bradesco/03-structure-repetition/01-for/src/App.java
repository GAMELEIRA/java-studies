public class App {
    public static void main(String[] args) throws Exception {
    
        // ESTRUTURA DE REPETICAO FOR EXECUTA BLOCO DE CODIGO
        //BASEANDO-SE EM UM CONDICAO ONDE UM CONTADOR Ã‰ AUMENTADO OU DECREMENTADO 
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }

    }
}
